void	*ft_memchr(const void *s, int c, size_t n)
{
	register const unsigned char *p;

	if (n)
	{
		p = s;
		while (--n)
		{
			if (*p++ == c)
				return ((void *)(p - 1));
		}
	}
	return (0);
}
